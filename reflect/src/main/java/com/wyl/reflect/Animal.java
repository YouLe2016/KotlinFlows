package com.wyl.reflect;

import java.util.Objects;

public class Animal {
    public Animal() {
    }

    public Animal(String name) {
        this.name = name;
    }

    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Animal{" +
                "name='" + name + '\'' +
                '}';
    }


}